function changeImage() {

const img = document.getElementById
('myImage');

const currentSrc = img.getAttribute("src");

if (currentSrc === 'img/javascript.png') {

    img.setAttribute("src","img/java.png")

    img.setAttribute ("alt","aqui tem um img")

}else {
    img.setAttribute ("src","img/javascript.png");
    img.setAttribute ("alt","aqui tem uma img")
}

}

